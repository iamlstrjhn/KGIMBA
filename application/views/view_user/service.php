<div class="head container">
	<h4>Basic Life Insurance Program</h4>
	<p>KGI–MBA shall pay the benefits under the Certificate of Membership to its member and his/her beneficiary(s) in accordance with the following:</p>
</div>


<div class="hd container">
	<div class="divider"></div>
	<h5>Pre Existing Condition</h5>
	<p>Pre–existing condition is a condition for which the member received medical treatment or has consulted a physician for medical treatment or has been taking medications during the twelve (12) months prior to the effective date of membership or twelve (12) months prior to the date of last reinstatement.</p>
	 <ul class="collapsible" data-collapsible="accordion">
    <li>
      <div class="collapsible-header"><i class="material-icons">info_outline</i>PRE-EXISTING CONDITION OF A MEMBER</div>
      <div class="collapsible-body"><span>To maintain low contribution rates and to safeguard the funds of KGI–MBA, KGI–MBA is not liable to any loss (death, total and permanent disability) occurring in less than one year of membership or within twelve (12) months prior to the date of last reinstatement except if caused by accident and/or diseases acquired within that period as certified by a competent physician other than TWO THOUSAND PESOS (Php2,000) gratuity for loss occurring less than twelve (12) months from effectivity date of insurance coverage in case of member’s death or total and permanent disability.</span></div>
    </li>
    <li>
      <div class="collapsible-header"><i class="material-icons">info_outline</i>PRE-EXISTING CONDITION OF LEGAL DEPENDENTS</div>
      <div class="collapsible-body"><span>To maintain low contribution rates and to safeguard the funds of the KGI–MBA, KGI–MBA is not liable to any loss (death or TPD) occurring in less than one (1) year of membership except if caused by accident in case of a legal dependent’s death.</span></div>
    </li>
    <li>
     
    </li>
  </ul>
</div>

<div class="hd container">
	<div class="divider"></div>
	<h5>Natural or Non-accidental Death of Member</h5>
	<ul class="collapsible " data-collapsible="accordion">
    <li>
      <div class="collapsible-header"><i class="material-icons">info_outline</i>NATURAL OR NON-ACCIDENTAL DEATH OF MEMBER</div>
      <div class="collapsible-body"><span>Upon the death of the member due to natural or non–accidental causes, KGI–MBA  shall indemnify the beneficiary/ies the amount of: THIRTY THOUSAND PESOS (PhP 30,000) should the death occur in less than one (1) year of membership or from the date of last reinstatement, or SIXTY THOUSAND PESOS (Php 60,000), if otherwise.</span></div>
    </li>
    <li>
      <div class="collapsible-header"><i class="material-icons">info_outline</i>NATURAL OR NON-ACCIDENTAL DEATH OF LEGAL DEPENDENTS</div>
      <div class="collapsible-body"><span>To maintain low contribution rates and to safeguard the funds of KGI–MBA, KGI–MBA is not liable to any death claim due to natural death of legal dependents occurring in less than one year of membership or within twelve (12) months prior to the date of last reinstatement.<br><br>
      However, those with membership of one (1) year and above will receive the benefit amounting to TEN THOUSAND PESOS (Php 10,000) and FIVE THOUSAND PESOS (Php 5,000) for death of legal spouse and child respectively.</span></div>
    </li>
  </ul>
</div>

<div class="hd container">
	<div class="divider"></div>
	<h5>Accidental Death</h5>
	<p>Accidental death is defined as loss of life resulting directly, independently and exclusively of all other causes, of bodily injury effected solely by external, violent and accidental means, except in the case of drowning or if internal injury revealed by an autopsy, there is evidence of a visible contusion or wound on the exterior of the body occurring within one hundred eighty (180) days from date of such injury.</p>
	<ul class="collapsible" data-collapsible="accordion">
    <li>
      <div class="collapsible-header"><i class="material-icons">info_outline</i>ACCIDENTAL DEATH OF MEMBER</div>
      <div class="collapsible-body"><span>Upon death of the member due to accident, the beneficiary/ies will be indemnified an amount equal to ONE HUNDRED TWENTY THOUSAND PESOS (Php 120,000).</span></div>
    </li>
    <li>
      <div class="collapsible-header"><i class="material-icons">info_outline</i>ACCIDENTAL DEATH OF LEGAL DEPENDENTS</div>
      <div class="collapsible-body"><span>Upon death of the member’s legal spouse due to accident, KGI–MBA shall indemnify the member an amount equal to TWENTY THOUSAND PESOS (Php 20,000).<br><br>
      Upon death of the member’s child due to accident, KGI–MBA shall indemnify the member an amount equal to TEN THOUSAND PESOS (Php 10,000).</span></div>
    </li>
  </ul>
</div>

<div class="hd container">
	<div class="divider"></div>
	<h5>Total and Permanent Disablity</h5>
	<p>Total and permanent disability (TPD) shall mean disability caused by bodily injury or disease which prevents the member from engaging in any gainful activity and must continue uninterruptedly for at least six (6) months.<br><br>
 	There are two types of TPD coverage:</p>


	<ul class="collapsible " data-collapsible="accordion">
    <li>
      <div class="collapsible-header"><i class="material-icons">info_outline</i>Disability with dismemberment</div>
      <div class="collapsible-body"><span>The loss of both arms, or both legs, of one arm and one leg, or of both eyes, shall be considered total and permanent disability.  Loss of both arms and both legs shall mean dismemberment by amputation of the entire hand or foot; with respect to eyes, entire and irrecoverable loss of sight. This type of TPD can be settled immediately upon submission of complete claims documents.</span></div>
    </li>
    <li>
      <div class="collapsible-header"><i class="material-icons">info_outline</i>Disability without dismemberment</div>
      <div class="collapsible-body"><span>If a member or his/her spouse becomes sick or met an accident resulting to complete inability to engage in any gainful employment and becomes bedridden, he/she can be considered as total and permanent disabled if after six (6) months the health condition has not improved. After such confirmation of a competent physician, the member or his/her spouse will receive the full amount for TPD according to the Table of Benefits for TPD.</span></div>
    </li>
  </ul>

  	<ul class="hd collapsible" data-collapsible="accordion">
    <li>
      <div class="collapsible-header"><i class="material-icons">info_outline</i>TPD BENEFIT OF A MEMBER</div>
      <div class="collapsible-body"><span>If the member becomes totally and permanently disabled as confirmed by a competent physician, KGI–MBA  shall indemnify the member an amount equal to FIFTEEN THOUSAND PESOS (Php 15,000) should the disability occur in less than one year of membership or from the date of last reinstatement, or THIRTY THOUSAND PESOS (Php 30,000), if otherwise.<br><br>
      It is understood that upon full payment of the member’s total and permanent disability benefit, his/her certificate of membership is automatically terminated.</span></div>
    </li>
    <li>
      <div class="collapsible-header"><i class="material-icons">info_outline</i>TPD BENEFIT OF A LEGAL DEPENDENTS</div>
      <div class="collapsible-body"><span>To maintain low contribution rates and to safeguard the funds of KGI–MBA, KGI–MBA is not liable to any disability of the legal dependents occurring in less than one year of membership or within twelve (12) months from the date of last reinstatement.
      <br><br>
      However, if the disability of the member’s spouse occurs on the first year or onwards, KGI–MBA shall indemnify the member an amount equal to FIVE THOUSAND PESOS (Php 5,000) but KGI–MBA is not liable for the disability of the member’s child.</span></div>
    </li>
  </ul>
</div>

<div class="hd container">
	<div class="divider"></div>
	<h5>Hospital Bill Reimbursement Benefit (HBRB)</h5>
	<p>In case a member or his/her member’s legal dependents is hospitalized due to sickness or accident occurring after the date of membership and resulting in confinement for not less than 24 hours for treatment by a competent physician, KGI–MBA  will reimburse the amount up to a maximum benefit of:</p>

	<p>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp A. ONE THOUSAND FIVE HUNDRED PESOS (Php 1,500.00) for member per annum</p>
	<p>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp B. SIX HUNDRED PESOS (Php 600.00) for the member’s spouse per annum</p>
	<p>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp C. THREE HUNDRED PESOS (Php 300.00) for the member’s child for a maximum of three children per annum</p>

</div>

<div class="hd container">
	<div class="divider"></div>
	<h5>Member's Equity Value</h5>
	<p>(A). A member shall be entitled to an equity value equivalent to at least FIFTY PER CENTUM (50%) of his/her total gross contributions paid, interest shall be credited to the Equity Value annually at a rate to be determined by the Board of Trustees but in no case less than the prevailing savings rate of the commercial bank.  This equity value, inclusive of interest thereon, is payable upon termination of his/her membership from KGI–MBA including death and TPD.</p>
	<p>A surrender charge equal to THIRTY PER CENTUM (30%) of the Equity Value may however be imposed by KGI–MBA for member’s termination, occurring in the first three (3) years of membership including death and TPD.</p>
	<p>&nbsp</p>
	<p>(B). Upon reaching the termination age of sixty five (65) years old or seventy (70) years old for those who are at least (65) years old at the time of the effectivity of the Insurance Plan, the member shall be entitled to payment of the member’s Equity Value.</p>
</div>


