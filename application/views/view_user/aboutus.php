	<h3>Institutional Background</h3>

	<p>KGI started in Kalookan, Zambales and Malate by lending only to families wishing to improve their lives by building their business to source their daily needs. While multiplying the members, one of the problems facing the institution is the death of the member while it still has a loan. The management then started  to sought on how to provide risk protection to its members and their dependents. Thus, the KGI implemented MMF or the Members Mutual Fund. The MMF became the response to cover the remaining loan balance of the members and no need to pay more by his family.</p>
	<p>Over the years, KGI continually enhanced and increased the benefits of the members in MMF. Until not only the debt left by members of the MMF were answered but also a small amount were given to his family members to bury him/her properly. Further, a portion of their hospitalization  were also covered by their contribution to MMF.</p>
	<p>In  2008, KGI began to study about Mutual Benefit Association, a more stable institution that can provide the members and their legal dependents a benefit in case an unforeseen  event come to their lives.  An institution that is guided by the Insurance Commission (IC) and the Securities and Exchange Commission (SEC) and ensures that the institution meets the benefits promised to its members.</p>
	<p>Due to Joint IC-CDA-SEC Memorandum Circular No. 01-2011 of the Insurance Commission , the management and Board of Trustees of KGI decided to establish its own MBA through the help of RIMANSI, a Non—Government Organization who helps microfinance institutions to have their own MBA, in preparing the documents and doing assessment for KGI to be able to register KGI’s  MBA to SEC and have the license to operate to the Insurance Commission. In 2010, RIMANSI and KGI had the Memorandum of Agreement and completed the required documents to become a full MBA</p>
	<p>In September 23, 2011, KGI Mutual Benefit Association, Inc. was registered to the Securities and Exchange Commission with certification number CN201117039, and also received  its license from the Insurance Commission granted on the 14th of February, 2012 with license number 2012-2-0.</p>
	<p>With the help of a thorough study of the actuary for providing proper benefits for members and staff, the management is searching for other benefits to increase service and to meet their other insurance needs that will match in times of crisis.</p>
	<p>At this time, KGI and KGI MBA is committed to its mission and its true concern for the poor family. The program continues to grow and continuing serving the poor people through micro-financing and micro-insurance program.</p>
